# Intersection: Create
#  two sets with some 
# common elements 
# and perform union,
#  intersection,
#  and difference 
# operations.

set1={1,2,3,4,5,}
set2={3,4,5,6,7,}
union1=set1.union(set2)
diffrence1=set1.difference(set2)
diffrence2=set2.difference(set1)
intersection=set1.intersection(set2)


print(union1)
print(diffrence1)
print(diffrence2)
print(intersection)













